package com.HS.day03.condition.loop;

public class Exam_While {

	public static void main(String[] args) {
		/*
		 * 제어문 - 반복문
		 * 2. while
		 * 문법
		 * 초기식;
		 * while(조건식) {실행문장;}
		 * 
		 * 
		 * for(초기식; 조건식; 증감식){실행문장;}
		 * 
		 */
		// for문으로 1부터 10까지 출력해보자
		int num = 1;
		while(num<=10) {
			System.out.println(num);
			num++;
		}
	}

}
